
int exp;
Console.WriteLine("Enter the exp is::");
exp = Int32.Parse(Console.ReadLine());
switch(exp)
{
    case 1:
        Console.WriteLine("Case 1");
        break;
    case 2:
        Console.WriteLine("Case 2");
        break;
    case 3:
        Console.WriteLine("Case 3");
        break;
    default: Console.WriteLine("default case");
        break;
}